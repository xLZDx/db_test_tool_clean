def _extract_odi_using_subselect(final_select_sql: str) -> str:
    """Phase 7.5 helper retained for back-compat with existing tests.
    Phase 7.6 emitter no longer needs this -- DRD-driven projection
    does not project from ODI's USING(...) inner SELECT.  The helper
    is kept so existing unit tests that exercise the paren-walker
    invariants continue to pass."""
    if not final_select_sql:
        return ""
    upper = final_select_sql.upper()
    using_idx = upper.find("USING")
    if using_idx < 0:
        return ""
    paren_idx = final_select_sql.find("(", using_idx)
    if paren_idx < 0:
        return ""
    depth = 0
    end_idx = -1
    in_string = False
    in_line_comment = False
    in_block_comment = False
    s = final_select_sql
    i = paren_idx
    while i < len(s):
        c = s[i]
        if in_string:
            if c == "'":
                if i + 1 < len(s) and s[i + 1] == "'":
                    i += 2
                    continue
                in_string = False
            i += 1
            continue
        if in_line_comment:
            if c == "\n":
                in_line_comment = False
            i += 1
            continue
        if in_block_comment:
            if c == "*" and i + 1 < len(s) and s[i + 1] == "/":
                in_block_comment = False
                i += 2
                continue
            i += 1
            continue
        if c == "'":
            in_string = True
            i += 1
            continue
        if c == "-" and i + 1 < len(s) and s[i + 1] == "-":
            in_line_comment = True
            i += 2
            continue
        if c == "/" and i + 1 < len(s) and s[i + 1] == "*":
            in_block_comment = True
            i += 2
            continue
        if c == "(":
            depth += 1
        elif c == ")":
            depth -= 1
            if depth == 0:
                end_idx = i
                break
        i += 1
    if end_idx < 0:
        return ""
    return final_select_sql[paren_idx + 1:end_idx].strip()
